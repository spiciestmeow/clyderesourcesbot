def paginate(text: str, page_size: int = 900) -> list:
    lines = text.split("\n")
    pages = []
    current = ""
    for line in lines:
        if len(current) + len(line) + 1 > page_size:
            pages.append(current.strip())
            current = line + "\n"
        else:
            current += line + "\n"
    if current.strip():
        pages.append(current.strip())
    return pages

